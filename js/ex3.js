let num1=parseInt(prompt('Enter first number:'));
let num2=parseInt(prompt('Enter second number:'));


let add=num1+num2;
console.log(`Addition of ${num1} and ${num2} is:${add}`)
let subtract=num1-num2
console.log(`Substraction of ${num1} and ${num2} is:${subtract}`)
let multiply=num1*num2
console.log(`Multiplication of ${num1} and ${num2} is:${multiply}`)
let divide=num1/num2
console.log(`Dividing ${num1} by ${num2} we get:${divide}`)
let modulo=num1%num2
console.log(`Remainder on dividing ${num1} by ${num2} is:${modulo}`)